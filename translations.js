document.addEventListener('DOMContentLoaded', () => {
    const langToggle = document.getElementById('lang-toggle');
    let currentLang = localStorage.getItem('royara_lang') || 'en';

    function setLanguage(lang) {
        currentLang = lang;
        localStorage.setItem('royara_lang', lang);

        // Update toggle button text and flag
        const toggleText = lang === 'en' ? 'AR' : 'EN';
        const toggleFlag = lang === 'en' ? 'uae-flag.svg' : 'uk-flag.svg';
        langToggle.innerHTML = `${toggleText} <img src="assets/${toggleFlag}" alt="${toggleText} Flag" class="nav-flag">`;

        // Translate elements
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                if (el.innerHTML.includes('<br>')) {
                    el.innerHTML = translations[lang][key];
                } else {
                    // For elements with icons (like features), we only replace the text node
                    const icon = el.querySelector('i');
                    if (icon) {
                        el.innerHTML = '';
                        el.appendChild(icon);
                        el.appendChild(document.createTextNode(' ' + translations[lang][key]));
                    } else {
                        el.textContent = translations[lang][key];
                    }
                }
            }
        });

        // Optional: you can apply a tiny class to adjust fonts if Arabic fonts need specific line-heights, 
        // but user requested "don't change any layouts or anything", so relying strictly on text swap.
        if (lang === 'ar') {
            document.body.classList.add('lang-ar');
        } else {
            document.body.classList.remove('lang-ar');
        }
    }

    // Initialize with saved or default language
    setLanguage(currentLang);

    // Add click listener to toggle
    langToggle.addEventListener('click', (e) => {
        e.preventDefault(); // prevent jumping
        const newLang = currentLang === 'en' ? 'ar' : 'en';
        setLanguage(newLang);
    });
});
